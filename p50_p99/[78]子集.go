func subsets(nums []int) [][]int {
	n := len(nums)
	resLen := 1 << n
	result := make([][]int, resLen)
	for i := 0; i < resLen; i++ {
		res := []int{}
		for j := 0; j < n; j++ {
			if i&(1<<j) > 0 {
				res = append(res, nums[j])
			}
		}
		result[i] = append([]int{}, res...)
	}
	return result
}

