func eliminate(board string) string {
	flag := true // æ˜¯å¦éœ€è¦ç»§ç»­éå†
	for flag && len(board) > 0 {
		flag = false
		count := 1
		length := len(board)
		for i := 1; i < length; i++ {
			if board[i] == board[i-1] {
				count++
			} else {
				if count >= 3 {
					board = board[:i-count] + board[i:]
					flag = true
					break
				}
				count = 1
			}
		}
		if count >= 3 {
			board = board[:length-count]
			flag = true
		}
	}
	return board
}

