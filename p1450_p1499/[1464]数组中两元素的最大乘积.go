func maxProduct(nums []int) int {
	a, b := nums[0], nums[1]
	if b > a {
		a, b = b, a
	}
	for i, l := 2, len(nums); i < l; i++ {
		if nums[i] > a {
			b, a = a, nums[i]
		} else if nums[i] > b {
			b = nums[i]
		}
	}
	return (a - 1) * (b - 1)
}
